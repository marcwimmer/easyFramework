	public class efHtmlTextNode : efHTMLElement
	{
		
		
		//================================================================================
		//Private Fields:
		//================================================================================
		private string msText;
		
		//================================================================================
		//Public Properties:
		//================================================================================
		public string sText
		{
			get{
				return msText;
			}
			set
			{
				msText = value;
			}
		}
		
		//================================================================================
		//Public Methods:
		//================================================================================
		
		//================================================================================
//Sub:       New
		//--------------------------------------------------------------------------------'
//Purpose:   constructor
		//--------------------------------------------------------------------------------'
//Params:    the parent-object
		//--------------------------------------------------------------------------------'
//Created:   23.03.2004 19:15:01
		//--------------------------------------------------------------------------------'
//Changed:
		//--------------------------------------------------------------------------------'
		public efHtmlTextNode(efHTMLElement oParent) : base(oParent) {
		}
		public efHtmlTextNode() : base(null) {
		}
		
		//================================================================================
//Function:  sGetNodeName
		//--------------------------------------------------------------------------------'
//Purpose:   gets the nodename
		//--------------------------------------------------------------------------------'
//Params:
		//--------------------------------------------------------------------------------'
//Returns:   string
		//--------------------------------------------------------------------------------'
//Created:   23.03.2004 19:14:45
		//--------------------------------------------------------------------------------'
//Changed:
		//--------------------------------------------------------------------------------'
		public override string sGetNodeName()
		{
			return "";
		}
		
		
		//================================================================================
//Function:  asAllowedParentTags
		//--------------------------------------------------------------------------------'
//Purpose:   returns the allowed parent-tags
		//--------------------------------------------------------------------------------'
//Params:    '
		//--------------------------------------------------------------------------------'
//Returns:   string-list
		//--------------------------------------------------------------------------------'
//Created:   23.03.2004 18:48:37
		//--------------------------------------------------------------------------------'
//Changed:
		//--------------------------------------------------------------------------------'
		public override string[] asAllowedParentTags()
		{
			string[] asResult = new string[] { "P", "LI", "BODY", "FORM", "TD", "SPAN", "BUTTON", "TEXTAREA", "OPTION", "LABEL", "DIV", "A" };
			
			return asResult;
		}
		
		public override void gRenderBeginTag (FastString sHtmlBuilder, int lLevel)
		{
			sHtmlBuilder.Append(sText);
			
		}
		
		
		public override void gRenderEndTag (FastString output, int lLevel)
		{
			
		}
	}
	
}
