	public class XmlAttribute
	{
		//================================================================================
		//private fields:
		//================================================================================
		private System.Xml.XmlAttribute moAttribute;
		
		//================================================================================
		//public properties:
		//================================================================================
		
		//================================================================================
		// Property:     sText
		//--------------------------------------------------------------------------------
		// Purpose:      innertext
		//--------------------------------------------------------------------------------
		// Created:      21.03.04 M.Wimmer (mwimmer@promain-software.de)
		//--------------------------------------------------------------------------------
		// Changed:
		//================================================================================
		public string sText
		{
			get{
				return moAttribute.InnerText;
			}
			set
			{
				moAttribute.InnerText = value;
			}
		}
