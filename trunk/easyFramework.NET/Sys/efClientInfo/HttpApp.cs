	namespace Webobjects
	{
		
		
		public class HttpApp
		{
			
			
			//================================================================================
			//Private Fields:
			//================================================================================
			private System.Web.HttpApplicationState moWebApp;
			//================================================================================
			//Public Consts:
			//================================================================================
			
			//================================================================================
			//Public Properties:
			//================================================================================
			
			//================================================================================
			//Public Events:
			//================================================================================
			
			//================================================================================
			//Public Methods:
			//================================================================================
			public HttpApp(System.Web.HttpApplicationState oApp) {
				moWebApp = oApp;
			}
			public object oGet(string sName)
			{
				return moWebApp[sName];
			}
			public object oGet(int lIndex)
			{
				return moWebApp[lIndex];
			}
			
			public string sApplicationPath()
			{
				return easyFramework.Sys.ToolLib.DataConversion.gsCStr(moWebApp["sWebPageRoot"]);
			}
			public System.Web.HttpApplicationState oHttpApplication
			{
				get{
					return moWebApp;
				}
			}
			
			//================================================================================
