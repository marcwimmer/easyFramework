			//================================================================================
			//Public Events:
			//================================================================================
			
			//================================================================================
			//Public Methods:
			//================================================================================
			public HttpRequest(System.Web.HttpRequest oRequest) {
				moRequest = oRequest;
			}
			public object oGet(string sName)
			{
				return moRequest[sName];
			}
			
			
			public System.Web.HttpRequest oHttpRequest
			{
				get{
