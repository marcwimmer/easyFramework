	public class efHtmlTextarea : efHTMLElement
	{
		
		
		
		//================================================================================
		//Private Fields:
		//================================================================================
		private string msText;
		
		
		
		//================================================================================
		//Public Properties:
		//================================================================================
		
		//================================================================================
		//Property:  sText
		//--------------------------------------------------------------------------------'
		//Purpose:   the text uses an underlying textnode-element
		//--------------------------------------------------------------------------------'
		//Params:    -
		//--------------------------------------------------------------------------------'
		//Created:   23.03.2004 21:44:49
		//--------------------------------------------------------------------------------'
		//Changed:
		//--------------------------------------------------------------------------------'
		public string sText
		{
			get{
				return msText;
			}
			set
			{
				msText = value;
			}
		}
		
		//================================================================================
		//Public Methods:
		//================================================================================
		
		//================================================================================
//Sub:       New
		//--------------------------------------------------------------------------------'
//Purpose:   constructor
		//--------------------------------------------------------------------------------'
//Params:    the parent-object
		//--------------------------------------------------------------------------------'
//Created:   23.03.2004 19:15:01
		//--------------------------------------------------------------------------------'
//Changed:
		//--------------------------------------------------------------------------------'
		public efHtmlTextarea(efHTMLElement oParent) : base(oParent) {
		}
		public efHtmlTextarea() : base(null) {
		}
		
		//================================================================================
//Function:  sGetNodeName
		//--------------------------------------------------------------------------------'
//Purpose:   gets the nodename
		//--------------------------------------------------------------------------------'
//Params:
		//--------------------------------------------------------------------------------'
//Returns:   string
		//--------------------------------------------------------------------------------'
//Created:   23.03.2004 19:14:45
		//--------------------------------------------------------------------------------'
//Changed:
		//--------------------------------------------------------------------------------'
		public override string sGetNodeName()
		{
			return "TEXTAREA";
		}
		
		
		//================================================================================
//Function:  asAllowedParentTags
		//--------------------------------------------------------------------------------'
//Purpose:   returns the allowed parent-tags
		//--------------------------------------------------------------------------------'
//Params:    '
		//--------------------------------------------------------------------------------'
//Returns:   string-list
		//--------------------------------------------------------------------------------'
//Created:   23.03.2004 18:48:37
		//--------------------------------------------------------------------------------'
//Changed:
		//--------------------------------------------------------------------------------'
		public override string[] asAllowedParentTags()
		{
			string[] asResult = new string[] { "P", "LI", "BODY", "FORM", "TD", "SPAN", "DIV" };
			
			return asResult;
		}
		
		
		
		//================================================================================
//Sub:       gRenderBeginTag
		//--------------------------------------------------------------------------------'
//Purpose:   creates text-area element
		//--------------------------------------------------------------------------------'
//Params:    shtml-writer
		//--------------------------------------------------------------------------------'
//Created:   24.03.2004 15:11:42
		//--------------------------------------------------------------------------------'
//Changed:
		//--------------------------------------------------------------------------------'
		public override void gRenderBeginTag (FastString sHtmlBuilder, int lLevel)
		{
			
			
			sHtmlBuilder.Append(msGetIndent(lLevel) + "<TEXTAREA ");
			if (moAttributeList != null)
			{
				
				foreach (efAttribute oAttr in moAttributeList)
				{
					if (oAttr.bIsEmpty == false)
					{
						
						sHtmlBuilder.Append(" " + oAttr.sName + "=\"" + easyFramework.Sys.ToolLib.DataConversion.gsCStr(Functions.Replace(oAttr.sValue, "\"", efHTML_QUOT)) + "\"");
						
					}
					
				}
			}
			
			sHtmlBuilder.Append(">");
			
			sHtmlBuilder.Append(sText);
			
			sHtmlBuilder.Append("</TEXTAREA>" + "\n");
			
		}
		
		//================================================================================
//Sub:       gRenderBeginTag
		//--------------------------------------------------------------------------------'
//Purpose:   creates text-area element
		//--------------------------------------------------------------------------------'
//Params:    shtml-writer
		//--------------------------------------------------------------------------------'
//Created:   24.03.2004 15:11:42
		//--------------------------------------------------------------------------------'
//Changed:
		//--------------------------------------------------------------------------------'
		public override void gRenderEndTag (FastString sHtmlBuilder, int lLevel)
		{
			//no end tag here
		}
	}
	
}
