	//--------------------------------------------------------------------------------'
//Changed:
	//--------------------------------------------------------------------------------'
	public class efHtmlButton : efHTMLElement
	{
		
		
		
		//================================================================================
		//Private Fields:
		//================================================================================
		efHtmlTextNode moChildTextNode;
		
		
		//================================================================================
		//Public Properties:
		//================================================================================
		
		//================================================================================
//Property:  sText
		//--------------------------------------------------------------------------------'
//Purpose:   the button's caption
		//--------------------------------------------------------------------------------'
//Params:    -
		//--------------------------------------------------------------------------------'
//Created:   24.03.2004 15:33:40
		//--------------------------------------------------------------------------------'
//Changed:
		//--------------------------------------------------------------------------------'
		public string sText
		{
			get{
				if (moChildTextNode == null)
				{
					return "";
				}
				else
				{
					return moChildTextNode.sText;
				}
				
			}
			set
			{
				if (moChildTextNode == null)
				{
					moChildTextNode = new efHtmlTextNode(this);
					
				}
				moChildTextNode.sText = value;
				
			}
		}
		
		
		//================================================================================
		//Public Methods:
		//================================================================================
		
		//================================================================================
//Sub:       New
		//--------------------------------------------------------------------------------'
//Purpose:   constructor
		//--------------------------------------------------------------------------------'
//Params:    the parent-object
		//--------------------------------------------------------------------------------'
//Created:   23.03.2004 19:15:01
		//--------------------------------------------------------------------------------'
//Changed:
		//--------------------------------------------------------------------------------'
		public efHtmlButton(efHTMLElement oParent) : base(oParent) {
			
		}
		public efHtmlButton() : base(null) {
		}
		
		//================================================================================
//Function:  sGetNodeName
		//--------------------------------------------------------------------------------'
//Purpose:   gets the nodename
		//--------------------------------------------------------------------------------'
//Params:
		//--------------------------------------------------------------------------------'
//Returns:   string
		//--------------------------------------------------------------------------------'
//Created:   23.03.2004 19:14:45
		//--------------------------------------------------------------------------------'
//Changed:
		//--------------------------------------------------------------------------------'
		public override string sGetNodeName()
		{
			return "BUTTON";
		}
		
		
		//================================================================================
//Function:  asAllowedParentTags
		//--------------------------------------------------------------------------------'
//Purpose:   returns the allowed parent-tags
		//--------------------------------------------------------------------------------'
//Params:    '
		//--------------------------------------------------------------------------------'
//Returns:   string-list
		//--------------------------------------------------------------------------------'
//Created:   23.03.2004 18:48:37
		//--------------------------------------------------------------------------------'
//Changed:
		//--------------------------------------------------------------------------------'
		public override string[] asAllowedParentTags()
		{
			string[] asResult = new string[] { "FORM", "BODY", "TD", "SPAN", "P" };
			return asResult;
		}
	}
	
}
