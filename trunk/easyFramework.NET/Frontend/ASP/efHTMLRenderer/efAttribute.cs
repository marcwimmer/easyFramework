using System;
using easyFramework.Sys.ToolLib;

namespace easyFramework.Frontend.ASP.HTMLRenderer
{
	//================================================================================
	//Class:     efAttribute
	
	//--------------------------------------------------------------------------------'
	//Module:    efAttribute.vb
	//--------------------------------------------------------------------------------'
	//Copyright: Promain Software-Betreuung GmbH
	//--------------------------------------------------------------------------------'
	//Purpose:   an attribute of an html-element
	//--------------------------------------------------------------------------------'
	//Created:   23.03.2004 23:38:23
	//--------------------------------------------------------------------------------'
	//Changed:
	//--------------------------------------------------------------------------------'
	public class efAttribute
	{
		public efAttribute()
		{
			mbIsEmpty = true;
		}
		
		
		
		//================================================================================
		//Private Fields:
		//================================================================================
		private bool mbIsEmpty;
		private string msName;
		private string msValue;
		
		
		
		//================================================================================
		//Public Properties:
		//================================================================================
		
		//================================================================================
		//Property:  sName
		//--------------------------------------------------------------------------------'
		//Purpose:   the attribute-name
		//--------------------------------------------------------------------------------'
		//Params:    -
		//--------------------------------------------------------------------------------'
		//Created:   23.03.2004 23:40:19
		//--------------------------------------------------------------------------------'
		//Changed:
		//--------------------------------------------------------------------------------'
		public string sName
		{
			get
			{
				return msName;
			}
			set
			{
				msName = value;
				
			}
		}
		
		//================================================================================
		//Property:  bIsEmpty
		//--------------------------------------------------------------------------------'
		//Purpose:   initialized value or not
		//--------------------------------------------------------------------------------'
		//Params:    -
		//--------------------------------------------------------------------------------'
		//Created:   23.03.2004 23:40:07
		//--------------------------------------------------------------------------------'
		//Changed:
		//--------------------------------------------------------------------------------'
		public bool bIsEmpty
		{
			get
			{
				return mbIsEmpty;
			}
			set
			{
				mbIsEmpty = value;
			}
		}
		
		
		//================================================================================
		//Property:  sValue
		//--------------------------------------------------------------------------------'
		//Purpose:   the value in string-format
		//--------------------------------------------------------------------------------'
		//Params:    -
		//--------------------------------------------------------------------------------'
		//Created:   23.03.2004 23:41:19
		//--------------------------------------------------------------------------------'
		//Changed:
		//--------------------------------------------------------------------------------'
		public string sValue
		{
			get
			{
				return msValue;
			}
			set
			{
				msValue = value;
				if (!Functions.IsEmptyString(msValue))
				{
					mbIsEmpty = false;
				}
			}
		}
		
		
	}
	
}
