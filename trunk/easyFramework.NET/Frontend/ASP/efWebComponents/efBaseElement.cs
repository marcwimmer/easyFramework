		public ClientInfo oClientInfo
		{
			get
			{
				return oGetClientInfoFromParent(this.Parent);
		
		
			}
		}
		public ClientInfo oGetClientInfoFromParent(System.Web.UI.Control oParent)
		{
			//try to get client-info from parent-page:
			efPage aDlgPage;
			if (oParent.Page is efPage)
			{
				aDlgPage = ((efPage)(oParent.Page));
				return aDlgPage.oClientInfo;
			}
	
			return null;
	
		}
